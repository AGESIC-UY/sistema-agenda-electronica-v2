package uy.gub.sae.autocompletados.business.ejb;

import java.io.Serializable;

public class WarningAutocompletado implements Serializable {

	private static final long serialVersionUID = -1048369395441349295L;
	
	private String codigo;
	private String mensaje;

	public WarningAutocompletado() {
		super();
		// TODO Auto-generated constructor stub
	}

	public WarningAutocompletado(String codigo, String mensaje) {
		super();
		this.codigo = codigo;
		this.mensaje = mensaje;
	}

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}

	public String getMensaje() {
		return mensaje;
	}

	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}
	
}
